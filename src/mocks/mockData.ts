function parseIncotermsList(value: string): string[] {
  return Array.from(
    new Set(
      value
        .split(/[/,]/)
        .map((item) => item.trim().toUpperCase())
        .filter(Boolean),
    ),
  )
}

function getResponseTimeBucket(value: string): ResponseTimeBucket {
  const normalized = value.replace(/\s+/g, '').toLowerCase()
  if (normalized.includes('24') && !normalized.includes('48') && !normalized.includes('72')) return 'fast'
  if (normalized.includes('24–48') || normalized.includes('24-48') || normalized.includes('48')) return 'standard'
  return 'extended'
}

