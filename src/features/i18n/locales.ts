export const LOCALE_DICTS: Record<LocaleCode, TranslationDict> = { ru, en, kz, cn }

export function detectBrowserLocale(): LocaleCode {
  if (typeof navigator === 'undefined') return DEFAULT_LOCALE
  const raw = (navigator.languages?.[0] ?? navigator.language ?? '').toLowerCase()
  if (raw.startsWith('en')) return 'en'
  if (raw.startsWith('kk') || raw.startsWith('kz')) return 'kz'
  if (raw.startsWith('zh')) return 'cn'
  return 'ru'
}
